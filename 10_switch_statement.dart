void main(List<String> args) {
  int mark = 20;

  switch (mark) {
    case 1:
      print("$mark is one");
      break;
    case 2:
      print("$mark is two");
      break;
    case 3:
      print("$mark is three");
      break;
    default:
      print("$mark is more than the numbers that we know.");
      break;
  }
}
